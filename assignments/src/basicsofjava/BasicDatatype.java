package basicsofjava;

public class BasicDatatype {
	   public static void main(String args[]) {
	      
		   int num1 = 1;
		      int num2 = 2;
		      int resultInt = num1 + num2;

		      System.out.println("Int: " + resultInt);
		      
		      char character = 'A';

		      System.out.println("Char: " + character);   
		      
		      long longNum1 = 1L;
		      long longNum2 = 2L;
		      long resultLong = longNum1 + longNum2;
		      
		      System.out.println("Long: " + resultLong);
		      
		      short shortNum1 = 1;
		      short shortNum2 = 2;
		      short resultShort = (short)(shortNum1 + shortNum2);

		      System.out.println("Short: " + resultShort);

		      byte byteNum1 = 1;
		      byte byteNum2 = 2;
		      byte resultByte = (byte)(byteNum1 + byteNum2);

		      System.out.println("Byte: " + resultByte);
		      
		      
		      boolean boolValue = true;

		      System.out.println("Boolean: " + boolValue);

		      double doubleNum1 = 1.0;
		      double doubleNum2 = 2.0;
		      double resultDouble = doubleNum1 + doubleNum2;

		      System.out.println("Double: " + resultDouble);
		      
		      
		      float floatNum1 = 1.0f;
		      float floatNum2 = 2.0f;
		      float resultFloat = floatNum1 + floatNum2;

		      System.out.println("Float: " + resultFloat);

	  
	   }
	}
